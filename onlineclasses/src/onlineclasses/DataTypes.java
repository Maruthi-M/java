package onlineclasses;

public class DataTypes {
	public static void main(String args[])
	{
		byte b=10;//1 bytes-8bits
		short s=100;//2bytes
		int i=10000;//4 bytes
		float f=2.3f;//4bytes
		double d=908.90;//8 bytes
		char a='A';//2 bytes
		System.out.println("byte->"+b+"short->"+s+"int->"+i+"float->"+f+"double->"+d+"char->"+a);
	}

}
